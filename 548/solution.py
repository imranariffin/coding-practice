class Solution(object):
  def splitArray(self, nums):
    """
    :type nums: List[int]
    :rtype: bool
    """
    N = len(nums)

    sums = [0]*(N+1)
    for i in range(N):
      sums[i+1] = nums[i]+sums[i]

    for j in range(3, N-3):
      sameset = set()
      for i in range(1, j-1):
        s0 = sums[i]
        s1 = sums[j] - sums[i+1]
        if s0 == s1:
          sameset.add(s1)

      for k in range(j+2, N-1):
        s2 = sums[k] - sums[j+1]
        s3 = sums[N] - sums[k+1]

        if s3 == s2:
          if s2 in sameset:
            return True
    return False


